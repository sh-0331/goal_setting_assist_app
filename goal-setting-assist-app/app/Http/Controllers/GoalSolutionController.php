    {
        $solution = new Solution();
        // dd($goal);
        $solution->goals_id = $goal;
        $solution->content = $request->input('content');
        $solution->eval = $request->input('eval');
        $solution->save();

        return redirect()->route('goals.show', compact('goal'));
    }

